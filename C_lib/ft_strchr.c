#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int	idx;

	idx = 0;
	while (s[idx])
	{
		if (s[idx] == c)
			return ((char *)(s + idx));
		else
			idx++;
	}
	return (NULL);
}
